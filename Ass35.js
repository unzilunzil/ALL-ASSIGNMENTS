"use strict";
// List of animals with a common characteristic
const animals = ['Dog', 'Cat', 'Rabbit'];
// Loop to print animal names and statements
for (let i = 0; i < animals.length; i++) {
    const animalName = animals[i];
    console.log(`A ${animalName.toLowerCase()} would make a great pet.`);
}
// Statement about what these animals have in common
console.log(`\nAny of these animals would make a great pet!`);
